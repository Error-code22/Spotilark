const isDev = !app.isPackaged;

function findBinary(name, envVar, devPaths, prodRelative) {
  // 1. Check env variable
  const envPath = process.env[envVar];
  if (envPath && fs.existsSync(envPath)) return envPath;

  // 2. Production: bundled in resources
  if (!isDev) {
    const bundled = path.join(process.resourcesPath, prodRelative);
    if (fs.existsSync(bundled)) return bundled;
  }

  // 3. Dev: check common locations
  for (const p of devPaths) {
    if (fs.existsSync(p)) return p;
  }

  // 4. Fallback
  return devPaths[0] || name;
}

const YTDLP_PATH = findBinary('yt-dlp', 'YTDLP_PATH', [
  path.join(os.homedir(), 'AppData', 'Local', 'Programs', 'yt-dlp.exe'),
  'yt-dlp.exe',
], 'yt-dlp.exe');

const FFMPEG_PATH = findBinary('ffmpeg', 'FFMPEG_PATH', [
  path.join(os.homedir(), 'AppData', 'Local', 'Programs', 'ffmpeg', 'bin', 'ffmpeg.exe'),
  'ffmpeg.exe',
], 'ffmpeg');

const COOKIE_DIR = path.join(os.tmpdir(), 'spotilark-cookies');
const COOKIE_PATH = path.join(COOKIE_DIR, 'youtube-main.txt');

// Set env for Next.js server routes
process.env.YTDLP_PATH = YTDLP_PATH;
process.env.FFMPEG_PATH = FFMPEG_PATH;

let mainWindow;

function createWindow() {
  // Build a minimal menu with useful items (Reload, Force Reload, DevTools)
  const menuTemplate = [
    {
      label: 'File',
      submenu: [
        { label: 'Reload', accelerator: 'CmdOrCtrl+R', click: () => mainWindow?.webContents.reload() },
        { label: 'Force Reload', accelerator: 'CmdOrCtrl+Shift+R', click: () => mainWindow?.webContents.reloadIgnoringCache() },
        { type: 'separator' },
        { label: 'DevTools', accelerator: 'F12', click: () => mainWindow?.webContents.toggleDevTools() },
        { type: 'separator' },
        { role: 'quit' },
      ],
    },
    {
      label: 'View',
      submenu: [
        { role: 'resetZoom' },
        { role: 'zoomIn' },
        { role: 'zoomOut' },
        { type: 'separator' },
        { role: 'togglefullscreen' },
      ],
    },
  ];
  Menu.setApplicationMenu(Menu.buildFromTemplate(menuTemplate));

  mainWindow = new BrowserWindow({
    width: 1400,
    height: 900,
    minWidth: 1000,
    minHeight: 600,
    title: 'Spotilark',
    icon: path.join(__dirname, '../public/icon.png'),
    webPreferences: {
      preload: path.join(__dirname, 'preload.js'),
      contextIsolation: true,
      nodeIntegration: false,
    },
    backgroundColor: '#000000',
  });

  if (isDev) {
    mainWindow.loadURL('http://localhost:9002');
    mainWindow.webContents.openDevTools({ mode: 'detach' });
  } else {
    const PORT = 9100;
    const appDir = path.join(__dirname, '..');

    console.log(`[Electron] Starting Next.js server in production...`);
    const next = require(path.join(appDir, '.next', 'standalone', 'node_modules', 'next'));
    const nextApp = next({ dev: false, dir: appDir, quiet: true });
    const handle = nextApp.getRequestHandler();

    nextApp.prepare().then(() => {
      const server = http.createServer(handle);
      server.listen(PORT, '127.0.0.1', () => {
        console.log(`[Electron] Next.js server ready on port ${PORT}`);
        mainWindow.loadURL(`http://localhost:${PORT}`);
      });
      server.on('error', (err) => {
        console.error('[Electron] Server error:', err.message);
        mainWindow.loadFile(path.join(__dirname, '../out/index.html'));
      });
    }).catch((err) => {
      console.error('[Electron] Failed to start Next.js:', err.message);
      mainWindow.loadFile(path.join(__dirname, '../out/index.html'));
    });
  }

  mainWindow.on('closed', () => { mainWindow = null; });
}

