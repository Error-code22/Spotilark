// Get cookie args for yt-dlp CLI
function getYtDlpCookieArgs() {
  if (fs.existsSync(COOKIE_PATH)) {
    return `--cookies "${COOKIE_PATH}"`;
  }
  return '';
}

// yt-dlp search
ipcMain.handle('yt-search', async (_, query) => {
  try {
    const cookieArgs = getYtDlpCookieArgs();
    const result = execSync(
      `"${YTDLP_PATH}" ${cookieArgs} --flat-playlist --dump-single-json --no-warnings "ytsearch20:${query}"`,
      { timeout: 30000, encoding: 'utf-8', stdio: ['pipe', 'pipe', 'pipe'] }
    );
    const data = JSON.parse(result);
    const entries = data.entries || [];
    return entries
      .filter(e => e.id && e.title)
      .map(e => ({
        id: e.id,
        title: e.title,
        channel: e.channel || e.uploader || 'Unknown',
        duration: e.duration || 0,
        thumbnail: e.thumbnails?.[0]?.url || `https://i.ytimg.com/vi/${e.id}/mqdefault.jpg`,
      }));
  } catch (e) {
    console.error('yt-search failed:', e.message);
    return [];
  }
});

// yt-dlp stream URL
ipcMain.handle('yt-get-stream-url', async (_, videoId) => {
  try {
    const cookieArgs = getYtDlpCookieArgs();
    const result = execSync(
      `"${YTDLP_PATH}" ${cookieArgs} --get-url -f "bestaudio[ext=m4a]/bestaudio[ext=mp3]/bestaudio" --no-warnings "https://www.youtube.com/watch?v=${videoId}"`,
      { timeout: 30000, encoding: 'utf-8', stdio: ['pipe', 'pipe', 'pipe'] }
    );
    return result.trim();
  } catch (e) {
    console.error('yt-get-stream-url failed:', e.message);
    return null;
  }
});

// yt-dlp download
ipcMain.handle('yt-download', async (_, { videoId, format }) => {
  const downloadDir = path.join(app.getPath('userData'), 'downloads');
  fs.mkdirSync(downloadDir, { recursive: true });

  const outputTemplate = path.join(downloadDir, `${videoId}.%(ext)s`);
  const isAudio = format === 'audio';
  const cookieArgs = getYtDlpCookieArgs();

  try {
    const ffmpegArg = fs.existsSync(FFMPEG_PATH) ? `--ffmpeg-location "${FFMPEG_PATH}"` : '';
    execSync(
      `"${YTDLP_PATH}" ${cookieArgs} ${ffmpegArg} ${isAudio ? '-x --audio-format mp3 --audio-quality 0' : '-f bestvideo[ext=mp4]+bestaudio[ext=m4a]/best --merge-output-format mp4'} -o "${outputTemplate}" --no-warnings "https://www.youtube.com/watch?v=${videoId}"`,
      { timeout: 120000, stdio: 'pipe' }
    );

    const files = fs.readdirSync(downloadDir).filter(f => f.startsWith(videoId));
    if (files.length > 0) {
      return path.join(downloadDir, files[0]);
    }
  } catch (e) {
    console.error('yt-download failed:', e.message);
  }
  return null;
});

// --- Path Security ---

const ALLOWED_FS_DIRS = [
  app.getPath('userData'),
  app.getPath('downloads'),
  app.getPath('music'),
  app.getPath('temp'),
  path.join(os.homedir(), '.spotilark'),
];

function isPathAllowed(targetPath) {
  const resolved = path.resolve(targetPath);
  const normalizedResolved = resolved.replace(/\\/g, '/').toLowerCase();
  return ALLOWED_FS_DIRS.some(dir => {
    const normalizedDir = path.resolve(dir).replace(/\\/g, '/').toLowerCase();
    return normalizedResolved.startsWith(normalizedDir + '/') || normalizedResolved === normalizedDir;
  });
}

function rejectUnsafePath(targetPath) {
  if (targetPath && (targetPath.includes('..') || targetPath.includes('\0'))) {
    return true;
  }
  return !isPathAllowed(targetPath);
}

// File system access
ipcMain.handle('fs-read-file', async (_, filePath) => {
  if (rejectUnsafePath(filePath)) {
    console.warn(`[Security] Blocked fs-read-file: ${filePath}`);
    throw new Error('Access denied: path outside allowed directories');
  }
  return fs.readFileSync(filePath);
});

ipcMain.handle('fs-read-dir', async (_, dirPath) => {
  if (rejectUnsafePath(dirPath)) {
    console.warn(`[Security] Blocked fs-read-dir: ${dirPath}`);
    throw new Error('Access denied: path outside allowed directories');
  }
  return fs.readdirSync(dirPath, { withFileTypes: true });
});

ipcMain.handle('fs-exists', async (_, filePath) => {
  if (rejectUnsafePath(filePath)) {
    return false;
  }
  return fs.existsSync(filePath);
});

ipcMain.handle('fs-write-file', async (_, filePath, data) => {
  if (rejectUnsafePath(filePath)) {
    console.warn(`[Security] Blocked fs-write-file: ${filePath}`);
    throw new Error('Access denied: path outside allowed directories');
  }
  const dir = path.dirname(filePath);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(filePath, data);
  return true;
});

ipcMain.handle('fs-mkdir', async (_, dirPath) => {
  if (rejectUnsafePath(dirPath)) {
    console.warn(`[Security] Blocked fs-mkdir: ${dirPath}`);
    throw new Error('Access denied: path outside allowed directories');
  }
  if (!fs.existsSync(dirPath)) fs.mkdirSync(dirPath, { recursive: true });
  return true;
});

