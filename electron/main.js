// --- Folder Scanning & Watching ---

const AUDIO_EXTENSIONS = new Set(['.mp3', '.m4a', '.flac', '.wav', '.ogg', '.aac', '.opus', '.wma', '.mp4', '.webm', '.mkv']);
const watchedFolders = new Map();

function scanFolderRecursive(dirPath, maxDepth = 5, currentDepth = 0) {
  const results = [];
  if (currentDepth > maxDepth) return results;
  try {
    const entries = fs.readdirSync(dirPath, { withFileTypes: true });
    for (const entry of entries) {
      if (entry.name.startsWith('.')) continue;
      const fullPath = path.join(dirPath, entry.name);
      if (entry.isDirectory()) {
        results.push(...scanFolderRecursive(fullPath, maxDepth, currentDepth + 1));
      } else if (entry.isFile()) {
        const ext = path.extname(entry.name).toLowerCase();
        if (AUDIO_EXTENSIONS.has(ext)) {
          try {
            const stat = fs.statSync(fullPath);
            results.push({
              path: fullPath,
              name: entry.name,
              size: stat.size,
              modified: stat.mtime.toISOString(),
            });
          } catch {}
        }
      }
    }
  } catch {}
  return results;
}

ipcMain.handle('dialog-select-folder', async () => {
  const result = await dialog.showOpenDialog(mainWindow, {
    properties: ['openDirectory'],
    title: 'Select Music Folder',
  });
  if (result.canceled || result.filePaths.length === 0) return null;
  return result.filePaths[0];
});

ipcMain.handle('fs-scan-folder', async (_, folderPath) => {
  console.log(`[Electron] Scanning folder: ${folderPath}`);
  const files = scanFolderRecursive(folderPath);
  console.log(`[Electron] Found ${files.length} audio files in ${folderPath}`);
  return files;
});

ipcMain.handle('folder-watch-start', async (_, folderPath) => {
  if (watchedFolders.has(folderPath)) return { success: true, alreadyWatching: true };

  const watcher = chokidar.watch(folderPath, {
    ignored: /(^|[\/\\])\.(?!spotilark)|node_modules/,
    persistent: true,
    ignoreInitial: true,
    depth: 5,
    awaitWriteFinish: { stabilityThreshold: 500, pollInterval: 100 },
  });

  const sendEvent = (type, filePath) => {
    if (mainWindow && !mainWindow.isDestroyed()) {
      mainWindow.webContents.send('folder-watch-event', {
        type,
        path: filePath,
        folder: folderPath,
        timestamp: Date.now(),
      });
    }
  };

  watcher
    .on('add', (p) => {
      const ext = path.extname(p).toLowerCase();
      if (AUDIO_EXTENSIONS.has(ext)) sendEvent('add', p);
    })
    .on('change', (p) => {
      const ext = path.extname(p).toLowerCase();
      if (AUDIO_EXTENSIONS.has(ext)) sendEvent('change', p);
    })
    .on('unlink', (p) => {
      const ext = path.extname(p).toLowerCase();
      if (AUDIO_EXTENSIONS.has(ext)) sendEvent('unlink', p);
    });

  watchedFolders.set(folderPath, watcher);
  console.log(`[Electron] Started watching: ${folderPath}`);
  return { success: true };
});

ipcMain.handle('folder-watch-stop', async (_, folderPath) => {
  const watcher = watchedFolders.get(folderPath);
  if (watcher) {
    await watcher.close();
    watchedFolders.delete(folderPath);
    console.log(`[Electron] Stopped watching: ${folderPath}`);
  }
  return { success: true };
});

ipcMain.handle('folder-watch-list', () => {
  return Array.from(watchedFolders.keys());
});

